#include "push_swap_bonus.h"

int	is_empty(char *str)
{
	int	i;
	int	cc;

	i = 0;
	cc = 0;
	while (str[i])
	{
		if (str[i] != ' ' && str[i] != '\t')
			cc++;
		i++;
	}
	return (cc);
}
